package com.tokensigning.common;

public class ObjData {
	private String data;
	private String type;
	private String sigOptions;

	public String getData() {
		return data;
	}

	public void setData(String data) {
		this.data = data;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getSigOptions() {
		return sigOptions;
	}

	public void setSigOptions(String sigOptions) {
		this.sigOptions = sigOptions;
	}
}